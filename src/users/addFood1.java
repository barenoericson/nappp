    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
           String Foodname = foodname.getText().trim();
    String Price = price.getText().trim().toLowerCase();
    String Cat = category.getSelectedItem().toString().trim();
    String Status = status.getSelectedItem().toString().trim();

    connectDB connect = new connectDB();

    if (Foodname.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please enter your Food Name!", "Error", JOptionPane.WARNING_MESSAGE);
    } else if (Price.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please enter your Price !", "Error", JOptionPane.WARNING_MESSAGE);
    } else if (Cat.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Please select a Food Category!", "Error", JOptionPane.WARNING_MESSAGE);
    }else if(Status.isEmpty()){
        JOptionPane.showMessageDialog(null, "Please enter a Status!", "Error", JOptionPane.WARNING_MESSAGE);
    } else {
     try {

            String sql = "INSERT INTO food (foodname, category, price, status) VALUES (?, ?, ?, ?)";
            int rowsInserted = connect.insertData(sql, Foodname, Price, Cat, Status);

            if (rowsInserted > 0) {
                JOptionPane.showMessageDialog(null, "Add Food Successfully!");

                new food().setVisible(true);

                JFrame frame = (JFrame) SwingUtilities.getWindowAncestor(foodname);
                if (frame != null) {
                    frame.dispose();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Added Food failed!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(null, "Database Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    }//GEN-LAST:event_addMouseClicked

