    private void updateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updateMouseClicked
          if (foodname.getText().isEmpty() ||  price.getText().isEmpty() || category.getSelectedItem() == null || 
    status.getSelectedItem() == null || fid.getText().isEmpty()) {

    JOptionPane.showMessageDialog(null, "All Fields Are Required!");

} else {
    connectDB cdb = new connectDB();
    try {
        int userId;
        try {
            userId = Integer.parseInt(fid.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid Food ID!", "Validation Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
 
            int rowsUpdated = cdb.updateData(
                "UPDATE food SET foodname = ?, category = ?, price = ?, status  = ? WHERE f_id = ?",
                foodname.getText(),   
                price.getText(),
                category.getSelectedItem().toString(),
                status.getSelectedItem().toString(),
                userId
            );

            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(null, "Food updated successfully!");
                this.dispose();  
                new food().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Update failed! Food ID not found.", "Update Error", JOptionPane.ERROR_MESSAGE);
            }
        
    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Database Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } finally {
        cdb.closeConnection();
    }
}
    }//GEN-LAST:event_updateMouseClicked

